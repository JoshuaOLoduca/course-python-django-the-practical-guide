def get_date(item):
    return item["date"]


def index(req):
    sorted_posts = sorted(postsDb, key=get_date)
    latest = sorted_posts[-3:]
    return render(req, "blog/index.html", {"posts": latest})


def posts(req):
    return render(req, "blog/all-posts.html", {"all_posts": postsDb})


def post(req, slug):
    found_post = next(post for post in postsDb if post["slug"] == slug)

    return render(req, "blog/post-detail.html", {"post": found_post})
